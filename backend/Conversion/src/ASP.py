#------------------ 復号処理 --------------------#
#str型のASP（数）をstr型文字に変換
def ASP_to_str(s: str) -> str:
    return ASPd[str(s)]


#str型暗号文(/区切りの62進数)を10進数変換し、list型にしたものを出力
def list_decode62(s: str) -> list:
    l = list(map(str, s.split("/")))
    return list(map(decode62, l))

