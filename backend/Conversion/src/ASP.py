#str型の文字をASP(数)に変換する関数
def str_to_ASP(s: str) -> str:
    return ASPe[s]

#平文strを入力しASP変換し(文字 → 数字)、list型にしたものを出力
def list_encode144(s: str) -> list:
    return list(map(str_to_ASP, s))

#RSA変換でlistで帰ってくる

#list型10進数暗号文を6/区切りの62進数(文字のASC)str表記
def list_encode_base62(l: list) -> str:
    return "/".join(list(map(encode62, l)))
