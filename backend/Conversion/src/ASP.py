#------------------------------ (10-62)進数変換 -------------------------------#
#10→62進数変換
def encode62(n: int) -> str:
    if (int(n/62)):
        return encode62(int(n/62)) + ASPd[str(n%62)]
    return ASPd[str(n%62)]

#62→10進数に変換
def decode62(s: str) -> int:
    x = 0
    for i in range(1,len(str(s))+1):
        x += int(ASPe[s[-i]])*(62**(i-1))
    return x
